package model;
//자바 빈
public class Student {
	//속성
	private int sno;
	private String name;
	//기본 생성자
	public Student() {}
    //getters/setters
	public int getSno() {return sno;}
	public void setSno(int sno) {		this.sno = sno;	}
	public String getName() {		return name;	}
	public void setName(String name) {		this.name = name;	}
		
	

}
